# Create a list of 1 to 10
numbers = list(range(1, 11))

# Print the first three numbers
first_three = numbers[:3]
print("The first three numbers are:", first_three)

# Print the last three numbers
last_three = numbers[-3:]
print("The last three numbers are:", last_three)

# Print every second number
every_second = numbers[::2]
print("Every second number is:", every_second)
